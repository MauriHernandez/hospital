    <h3>Misión</h3>
    <p>Establecer las políticas de Estado para que la población ejerza su derecho a la protección a la salud.</p>

    <h3>Visión</h3>
    <p>Un Sistema Nacional de Salud Universal, equitativo, integral, sustentable, efectivo y de calidad, con particular enfoque a los grupos de la población que viven en condición de vulnerabilidad, a través del fortalecimiento de la rectoría de la autoridad sanitaria y la intersectorialidad; de la consolidación de la protección y promoción de la salud y prevención de enfermedades, así como la prestación de servicios plurales y articulados basados en la atención primaria; la generación y gestión de recursos adecuados; la evaluación y la investigación científica, fomentando la participación de la sociedad con corresponsabilidad.</p>

    <h3>Políticas</h3>
    <ul class="policy-list">
        <li>Garantizar el acceso efectivo y oportuno a los servicios de salud, con énfasis en la prevención y la atención primaria.</li>
        <li>Promover la calidad, seguridad y humanización de la atención, con enfoque de derechos, género, interculturalidad y ciclo de vida.</li>
        <li>Fortalecer la gobernabilidad, la rectoría y la regulación del sistema de salud, con participación social y rendición de cuentas.</li>
        <li>Impulsar la innovación, la investigación y el desarrollo científico y tecnológico en salud.</li>
        <li>Fomentar la coordinación intersectorial y la articulación de los actores del sistema de salud, con base en el diálogo y la concertación.</li>
    </ul>
